package com.test.model;

public class Vocation {
    private int v_id;
    private String v_name;
    private Double v_salary;
    private int v_num;
    private int v_managementid;

    public int getV_id() {
        return v_id;
    }

    public void setV_id(int v_id) {
        this.v_id = v_id;
    }

    public String getV_name() {
        return v_name;
    }

    public void setV_name(String v_name) {
        this.v_name = v_name;
    }

    public Double getV_salary() {
        return v_salary;
    }

    public void setV_salary(Double v_salary) {
        this.v_salary = v_salary;
    }

    public int getV_num() {
        return v_num;
    }

    public void setV_num(int v_num) {
        this.v_num = v_num;
    }

    public int getV_managementid() {
        return v_managementid;
    }

    public void setV_managementid(int v_managementid) {
        this.v_managementid = v_managementid;
    }
}
